DROP TABLE ELEMENT;
DROP TABLE DUMMY_ENTITY;

CREATE TABLE DUMMY_ENTITY (
    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    NAME VARCHAR2(100)
);

CREATE TABLE ELEMENT (
    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    CONTENT VARCHAR(100),
    DUMMY_ENTITY_KEY NUMBER,
    DUMMY_ENTITY NUMBER
);
